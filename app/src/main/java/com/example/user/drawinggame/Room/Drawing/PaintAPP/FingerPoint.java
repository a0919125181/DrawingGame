package com.example.user.drawinggame.Room.Drawing.PaintAPP;


class FingerPoint {
    int x;
    int y;
    int strokeWidth;
    int color;

    FingerPoint(int x, int y, int strokeWidth, int color) {
        this.x = x;
        this.y = y;
        this.strokeWidth = strokeWidth;
        this.color = color;
    }
}
